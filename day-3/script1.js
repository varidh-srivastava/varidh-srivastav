
// using do-while loop
const array1=[1,2,3,4,5];
let count=0;
do{
    console.log(array1[count]);
    count++;
}while(count<=array1.length);

// using while loop
const array2=[1,2,3,4,5,6]
let count2 = 0;
while(count2<=array2.length)
{
    console.log(array2[count2])
    count2++
}

